// =============================================================================
// MAIN.RS — точка входа твоего бэкенда (сервера на Rust).
// Здесь мы: поднимаем веб-сервер, настраиваем маршруты (URL) и раздаём
// фронтенд (HTML/CSS/JS) и один API-эндпоинт /api/hello.
// =============================================================================

// -----------------------------------------------------------------------------
// use ... — подключаем из других crate (библиотек) то, что будем использовать.
// Без "use" пришлось бы каждый раз писать полное имя, например axum::Router::new().
// -----------------------------------------------------------------------------
use axum::{
    routing::get,
    Json, Router,
};
use serde::Serialize;
use std::net::SocketAddr;
use tower_http::services::ServeDir;

// -----------------------------------------------------------------------------
// Структура (struct) — это как "форма" или "шаблон" данных.
// Мы описываем, какие поля есть у нашего ответа от сервера.
// #[derive(Serialize)] — макрос: "автоматически научи эту структуру
// превращаться в JSON". Serde — библиотека сериализации (Rust ↔ JSON).
// -----------------------------------------------------------------------------
#[derive(Serialize)]
struct HelloResponse {
    // Поле "message" типа String — текст, который отправим на фронтенд.
    message: String,
}

// -----------------------------------------------------------------------------
// Функция, которая обрабатывает GET-запрос на /api/hello.
// Она ничего не принимает (нет параметров), возвращает Json<HelloResponse>.
// Json(...) — оборачивает нашу структуру в тип "ответ в формате JSON".
// -----------------------------------------------------------------------------
async fn hello_handler() -> Json<HelloResponse> {
    // Создаём значение нашей структуры: поле message = "Привет с бэкенда!".
    let body = HelloResponse {
        message: String::from("Привет с бэкенда!"),
    };
    // Возвращаем его как JSON. Rust сам превратит структуру в строку вида
    // {"message": "Привет с бэкенда!"} и отправит в браузер.
    Json(body)
}

// -----------------------------------------------------------------------------
// #[tokio::main] — макрос, который превращает обычную fn main() в асинхронную.
// Т.е. внутри main мы можем писать await и работать с async-функциями (сервер).
// Без этого мы не смогли бы вызвать axum::serve(..., app).await.
// -----------------------------------------------------------------------------
#[tokio::main]
async fn main() {
    // Маршрутизатор (Router): связывает URL-пути с функциями-обработчиками.
    // .route("/api/hello", get(hello_handler)) — когда приходит GET /api/hello,
    // вызывается функция hello_handler. "get" = только GET-запросы.
    let app = Router::new()
        .route("/api/hello", get(hello_handler))
        // .nest_service("/", ...) — всё, что не совпало с маршрутами выше,
        // отдаём как статические файлы из папки "static" (index.html, CSS, JS).
        // "/" значит "корень сайта", т.е. http://127.0.0.1:3000/ откроет static.
        .nest_service("/", ServeDir::new("static"));

    // Адрес, на котором слушаем: 127.0.0.1 = только этот компьютер, порт 3000.
    // SocketAddr — тип "адрес + порт" в Rust.
    let addr = SocketAddr::from(([127, 0, 0, 1], 3000));
    println!("Сервер запущен: открой в браузере http://127.0.0.1:3000");

    // axum::serve — запускаем сервер: слушаем на addr, обслуживаем запросы через app.
    // .await — ждём, пока сервер работает (он работает бесконечно, пока не остановишь).
    axum::serve(
        tokio::net::TcpListener::bind(addr).await.unwrap(),
        app,
    )
    .await
    .unwrap();
}
